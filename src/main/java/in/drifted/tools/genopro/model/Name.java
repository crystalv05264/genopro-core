package in.drifted.tools.genopro.model;

public class Name {

    private final String first;
    private final String middle;
    private final String last;
    private final String last2;

    public Name(String first, String middle, String last, String last2) {
        this.first = first;
        this.middle = middle;
        this.last = last;
        this.last2 = last2;
    }

    public String getFirst() {
        return first;
    }

    public String getMiddle() {
        return middle;
    }

    public String getLast() {
        return last;
    }

    public String getLast2() {
        return last2;
    }

}
